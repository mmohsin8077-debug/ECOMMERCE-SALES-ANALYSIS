#E-Commerce sales
#Find the total sales, total profit, and total number of orders.
SELECT * FROM ECOMMERCESALES;
SELECT ROUND(SUM(SALES),2) AS TOTAL_SALES,
ROUND(SUM(PROFIT),2) AS TOTAL_PROFIT,
COUNT(`ORDER ID`) AS TOTAL_PROFIT
FROM ECOMMERCESALES;

#Find the top 10 products generating the highest sales.
SELECT `PRODUCT NAME`,
SUM(SALES) AS `HIGHEST PRODUCT SALE` 
FROM ECOMMERCESALES
GROUP BY `PRODUCT NAME`
ORDER BY 2 DESC;

#Show total sales and profit for each region.
SELECT REGION,
ROUND(SUM(SALES),2) AS `TOTAL SALES`,
ROUND(SUM(PROFIT),2) AS `TOTAL PROFIT`
FROM ECOMMERCESALES
GROUP BY REGION;


#Find how many orders were placed using each payment mode.
SELECT `PAYMENT MODE`,
COUNT(`ORDER ID`) AS TOTAL_ORDER
FROM ECOMMERCESALES
GROUP BY 1;

#Calculate average discount offered in each category.

SELECT CATEGORY,
ROUND(AVG(DISCOUNT),2) AS AVG_DISCOUNT
FROM ECOMMERCESALES
GROUP BY 1;

#Top 5 Customers by Revenue
SELECT `CUSTOMER NAME`,
ROUND(SUM(SALES),2) AS REVENUE 
FROM ECOMMERCESALES
GROUP BY 1
ORDER BY REVENUE DESC
LIMIT 5;

#Calculate total sales for every month.
SELECT MONTHNAME(`ORDER DATE`) AS MONTHS,
ROUND(SUM(SALES),2) AS TOTAL_SALES_BY_MONTH 
FROM ECOMMERCESALES
GROUP BY 1;

#Find the city with highest total sales.
SELECT CITY,
ROUND(SUM(SALES),2) AS HIGHEST_SALES
FROM ECOMMERCESALES
GROUP BY CITY
ORDER BY HIGHEST_SALES DESC
LIMIT 1;

