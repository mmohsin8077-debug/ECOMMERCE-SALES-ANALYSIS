#What are the top-performing and lowest-performing products within each distinct business category based on total generated profit?"
SELECT CATEGORY,`PRODUCT NAME`,SUM(PROFIT) AS TOTAL_PROFIT,
RANK() OVER(PARTITION BY CATEGORY ORDER BY SUM(PROFIT)) AS RANK1
FROM ECOMMERCESALES
GROUP BY 1,2;

#Which specific product ranks as the second-highest sales generator within each individual product category, and how far behind is it from the top performer?
WITH NEWTABLE AS(
	SELECT 
		 CATEGORY,
		`PRODUCT NAME`,
		SUM(SALES) AS TOTAL_SALES,
        DENSE_RANK() OVER(PARTITION BY CATEGORY ORDER BY SUM(SALES) DESC)
        AS SALES_RANK
        FROM ECOMMERCESALES
        GROUP BY 1,2
)
SELECT CATEGORY ,
`PRODUCT NAME`, 
TOTAL_SALES
FROM NEWTABLE
WHERE SALES_RANK=2;

#What is the Month-over-Month (MoM) sales growth rate.
SELECT * FROM ECOMMERCESALES;

WITH MONTHLYSALES AS(
SELECT 
YEAR(`ORDER DATE`) AS YEARS,
MONTH(`ORDER DATE`) AS MONTHS,
ROUND(SUM(SALES),2) AS CURRENT_SALE
FROM ECOMMERCESALES
GROUP BY 1,2
),
CALCULATEDSALES AS(
	SELECT YEARS,
    MONTHS,
    CURRENT_SALE,
    LAG(CURRENT_SALE,1) OVER(ORDER BY YEARS,MONTHS) AS PREVIOUS_SALE
    FROM MONTHLYSALES
)
 SELECT YEARS,MONTHS,
ROUND(
((CURRENT_SALE-PREVIOUS_SALE)/NULLIF(PREVIOUS_SALE,0))*100,2
) AS MOM_GROWTH
FROM CALCULATEDSALES;

